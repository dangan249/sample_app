class UsersController < ApplicationController

  # before filters apply to every action in a controller
  # here we restrict the filter to act only on the :edit and :update actions
  before_filter :signed_in_user, only: [:index, :edit, :update, :destroy]
  before_filter :correct_user,   only: [:edit, :update]
  before_filter :admin_user,     only: :destroy

  def destroy
    User.find(params[:id]).destroy
    flash[:success] = "User destroyed."
    redirect_to users_url
  end

  def index
    @users = User.paginate(page: params[:page])
  end

  def new
    @user = User.new
  end

  def show
    @user = User.find(params[:id])
  end

  def create
    @user = User.new(params[:user])
    if @user.save
      sign_in @user
      flash[:success] = "Welcome to the Sample App!" 
      # flash.now here is wrong since we need the flash to stay on after a redirect
      redirect_to @user
    else
      flash.now[:error] = "Failed to Sign Up. Please try again !  " 
      # flash.now here so it disapper when user navigate to a different page
      render 'new'
    end
  end

  def edit
    @user = User.find(params[:id])
  end

  def update
    @user = User.find(params[:id])
    if @user.update_attributes(params[:user])
      # Handle a successful update.
      flash[:success] = "Profile updated"
      sign_in @user
      redirect_to @user
    else
      flash.now[:error] = "Failed to update" 
      render 'edit'
    end
  end

  private

    def signed_in_user
      unless signed_in?
        store_location # save the url to a session
        redirect_to signin_url, notice: "Please sign in."
      end
    end

    def correct_user
      @user = User.find(params[:id])
      redirect_to(root_path) unless current_user?(@user)
    end

    def admin_user
      redirect_to(root_path) unless current_user.admin?
    end

end
